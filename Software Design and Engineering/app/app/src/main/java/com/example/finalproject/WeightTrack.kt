package com.example.finalproject
//simple weighttrack class to keep track of id/weight/date
class WeightTrack {
    var id: Int = 0
    var weight: Int = 0
    var date: String = "01/01/2020"
    //constructor allowing the user to create a weighttrack class with just weight and date
    constructor(weight: Int, date: String)
    {
        this.id = id
        this.weight = weight
        this.date = date
    }

}